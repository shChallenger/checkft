#include "../str.h"

char	*ptr_ncpy(char *dst, const char *src, size_t n)
{
	while (*src && n)
	{
		*dst++ = *src++;
		n -= 1;
	}
	while (n--)
		*dst++ = 0;
	return (dst);
}
